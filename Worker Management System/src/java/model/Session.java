package model;

import java.sql.Date;

/**
 *
 * @author congfptu
 */
public class Session {
    int sessionId;
    Date date;

    public Session() {
    }

    public int getSessionId() {
        return sessionId;
    }

    public void setSessionId(int sessionId) {
        this.sessionId = sessionId;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    
}
