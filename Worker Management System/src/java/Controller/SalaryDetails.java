    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void processGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("UTF-8");
        SalaryDAO db=new SalaryDAO();
        String cnid=request.getParameter("id");
        String name=request.getParameter("name");
        if (cnid!=null){
         ArrayList<ReportSalaryHistory> rps =db.getSalaryHistoryByID(Integer.parseInt(cnid));
         request.setAttribute("name", name);
         request.setAttribute("rps", rps);
          request.getRequestDispatcher("view/salaryDetails.jsp").forward(request, response);
    }
    }

